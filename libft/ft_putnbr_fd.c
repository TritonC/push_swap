#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	char		a;
	long int	n;

	n = nb;
	if (fd < 0)
		return ;
	if (n < 0)
	{
		write(fd, "-", 1);
		n = n * -1;
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
		return ;
	}
	a = n + '0';
	write(fd, &a, 1);
}
